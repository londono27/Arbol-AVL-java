    public void dibujarArbol(Lienzo panel) {
        Lienzo lienzo = panel;
        lienzo.setBackground(new Color(255, 241, 246));
        lienzo.setSize(1110, 400);
        lienzo.setLocation(0, 0);
        contenido.removeAll();
        contenido.add(lienzo, BorderLayout.CENTER);
        contenido.revalidate();
        contenido.repaint();
    }
